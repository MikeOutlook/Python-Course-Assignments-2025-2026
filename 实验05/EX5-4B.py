import numpy as np

arr =np.arange(1,25)  #产生原始一维数组，将本行代码补充完整
print("一维数组:\n",arr)

arr2d1 =arr.reshape(3,8)  #产生第1个二维数组，将本行代码补充完整
print("二维数组1:\n",arr2d1)

arr2d2 =arr.reshape(4,6)  #产生第2个二维数组，将本行代码补充完整
print("二维数组2:\n",arr2d2)

arr2d3 =arr.reshape(6,4)  #产生第3个二维数组，将本行代码补充完整
print("二维数组3:\n",arr2d3)

